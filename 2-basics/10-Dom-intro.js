// What is DOM ?

// DOM (Document Object Model) is a programming interface for HTML and XML documents. It's a tree-like structure that represents the structure of a document, where each node represents an element, attribute, or piece of text.
// The DOM is used by web browsers, search engines, and other software to:
// Parse and render HTML documents
// Allow JavaScript to interact with web pages
// Enable dynamic updates and manipulation of web pages
// Provide a standard interface for programming languages to access and modify web documents
// The DOM is made up of:
// Elements (tags): Representing structural elements like headings, paragraphs, images, and links.
// Attributes: Providing additional information about elements, such as IDs, classes, and styles.
// Text: Representing the content of elements, like the text inside a paragraph or heading.
// Events: Allowing developers to respond to user interactions, like clicks and hover effects.
// By accessing and manipulating the DOM, developers can create dynamic, interactive web experiences!